package JavaFundamentals;

public class PrimitiveTypes {
    byte age = 30;

    //add underscore like a coma to separate every 3 digits.
    int viewsCounts = 123_456_789;

    //add an "L" to specify long
    long views = 3_123_456_789L;

    double price = 10.99;

    //add an "F" for float
    float total = 11.56F;

    char letter = 'a';

    boolean isEligible = false;
}
